name = "Jess"
print("Reversed Jess: {}".format(name[::-1]))

is_e_in_name = "e" in name
print("Is e in Jess: {}".format(is_e_in_name))

is_me_in_name = "me" in name
print("Is me in Jess: {}".format(is_me_in_name))

fullName = name + " Grimes"
print("My fullname: {}".format(fullName))

compare_names = (fullName == "Jess Grimes")
print("Comparing names: {}".format(compare_names))

